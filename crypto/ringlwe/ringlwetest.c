/* Perform Ring-LWE key exchange with random data 
   and verify that alice and bob derive the same shared key */

static int test_ringlwe(BIO *out, int nid) {

  RLWE_PAIR *alice = NULL;
  RLWE_REC *rec = NULL;
  RLWE_PUB *bob = NULL;
  RLWE_CTX *ctx = NULL;
  
  unsigned char *apubbuf = NULL, *bpubbuf = NULL;
  size_t apublen, bpublen;
  
  unsigned char *recbuf = NULL;
  size_t reclen;
  
  unsigned char *assbuf = NULL, *bssbuf = NULL;
  size_t asslen, bsslen;
  
  int i, ret = 0;
  
  ctx = RLWE_CTX_new (nid);
  if (ctx == NULL) {
    return ret;
  }
  
  alice = RLWE_PAIR_new(ctx);
  bob = RLWE_PUB_new(ctx);
  
  if ((alice == NULL) || (bob == NULL) || (ctx == NULL)) {
    goto err;
	}

  /* Alice generates a private and public key pair */
  BIO_puts(out, "Testing key generation\n");
    
  if (!RLWE_PAIR_generate_key(alice)) { 
    fprintf(stderr,"Error generating RLWE key\n");
    ret = 0;
    goto err;
  }
  
  /* Alice sends public key to Bob */
  apublen = i2o_RLWE_PUB(RLWE_PAIR_get_publickey(alice), &apubbuf);
  if (apublen <= 0) {
    fprintf(stderr, "Error in RINGLWE routines\n");
    ret = 0;
    goto err;
  }
  
  rec = NULL;

  /* Bob generates shared key, public key and reconciliation data */
  BIO_puts(out, "Testing Bob shared secret generation\n");
  
  bsslen = KDF1_SHA1_len*8;
  bssbuf = (unsigned char *)OPENSSL_malloc(bsslen);
  bsslen = RINGLWE_compute_key_bob(bssbuf, bsslen, &rec,
				   RLWE_PAIR_get_publickey(alice), bob, KDF1_SHA1);
  
  bpublen = i2o_RLWE_PUB(bob, &bpubbuf);
  RLWE_PUB_free(bob);
  bob = NULL;
  
  bob = RLWE_PUB_new(ctx);
  
  reclen = i2o_RLWE_REC(rec, &recbuf);
  RLWE_REC_free(rec);
  rec = NULL;
  
  printf("Reconciliation data:\n");
  for (i = 4; i < reclen; i++) {
    BIO_printf(out, "%02x", recbuf[i]);
  }
  BIO_puts(out, "\n");
  
  BIO_printf(out, "Bob's shared secret key (%d bytes):\n", (int) bsslen);
  for (i = 0; i < bsslen; i++) {
    BIO_printf(out, "%02x", bssbuf[i]);
  }
  BIO_puts(out, "\n");
  
  if (o2i_RLWE_PUB(&bob, bpubbuf, bpublen) == NULL) {
    fprintf(stderr, "Error in RINGLWE routines (Bob public key reconstruction)\n");
    ret = 0;
    goto err;
  }
  
  if (o2i_RLWE_REC(&rec, recbuf, reclen) == NULL) {
    fprintf(stderr, "Error in RINGLWE routines (Bob reconciliation reconstruction)\n");
    ret = 0;
    goto err;
  }
  
  /* Alice computes shared key */
  asslen = KDF1_SHA1_len*8;
  assbuf = (unsigned char *)OPENSSL_malloc(asslen);
  asslen = RINGLWE_compute_key_alice(assbuf, asslen, bob, rec, alice, KDF1_SHA1);
  BIO_printf(out, "Alice's shared secret key (%d bytes):\n", (int) asslen);
  for (i = 0; i < asslen; i++) {
    BIO_printf(out, "%02x", assbuf[i]);
  }
  BIO_puts(out, "\n");
  
  /* Compare Alice's and Bob's shared keys */
  if ((asslen == 0) || (bsslen == 0) ||
      (bsslen != asslen) || (memcmp(assbuf, bssbuf, asslen))) {
    BIO_printf(out, " failed\n\n");
    fprintf(stderr, "Error in RINGLWE routines (mismatched shared secrets)\n");
    ret = 0;
  } else {
    BIO_printf(out, "ok!\n");
    ret = 1;
  }
 err:
  
  ERR_print_errors_fp(stderr);
  
  OPENSSL_free(bssbuf);
  OPENSSL_free(assbuf);
  OPENSSL_free(apubbuf);
  OPENSSL_free(bpubbuf);
  OPENSSL_free(recbuf);
  RLWE_REC_free(rec);
  RLWE_PUB_free(bob);
  RLWE_PAIR_free(alice);
  RLWE_CTX_free(ctx);
  printf("-------------------------------------------------------------------\n");
  return (ret);
}

